    >
      <div
        @click="nextSlide"
        class="text-default font-bold text-defaultOrange text-vertical mb-10"
      >
        NEXT
      </div>
      <span
        class="bg-defaultOrange w-12 h-px block mx-1.5 transform rotate-90"
